def lime_patient_report(explanation, X_test, scaler,
                        feature_names, patient_idx: int = 0):
    """
    Printed LIME clinical report — mirrors the SHAP report for easy comparison.
    Shows original feature values + LIME weight + direction.
    """
    original = scaler.inverse_transform(X_test)[patient_idx]
    feat_weights = dict(explanation.as_list(label=1))

    # Map LIME's binned feature strings back to original feature names
    # LIME returns strings like "age > 55.00" or "chol <= 240.50"
    rows = []
    for i, fname in enumerate(feature_names):
        # Find the matching LIME feature (partial string match)
        matched_weight = 0.0
        matched_rule   = "—"
        for rule, weight in feat_weights.items():
            if fname in rule:
                matched_weight = weight
                matched_rule   = rule
                break
        rows.append({
            "Feature"   : fname,
            "Value"     : round(original[i], 2),
            "Rule"      : matched_rule,
            "LIME Wt"   : round(matched_weight, 4),
            "Direction" : "↑ Risk" if matched_weight > 0 else "↓ Risk"
        })

    report = pd.DataFrame(rows).sort_values("LIME Wt", key=abs, ascending=False)

    print(f"\n── LIME Clinical Report — Patient #{patient_idx} ──")
    print(report.to_string(index=False))

    risk_rows      = report[report["LIME Wt"] > 0]
    prot_rows      = report[report["LIME Wt"] < 0]
    top_risk       = risk_rows.iloc[0]["Feature"]  if len(risk_rows) > 0 else "None"
    top_protective = prot_rows.iloc[0]["Feature"]  if len(prot_rows)  > 0 else "None"
    print(f"\n  Top risk factor : {top_risk}")
    print(f"  Top protective  : {top_protective}")


# ── 9. PREDICTION INTERFACE ───────────────────────────────────────────────────
def predict_patient(model, scaler, patient_values: list,
                    threshold: float = 0.5) -> None:
    x = np.array(patient_values).reshape(1, -1)
    x_scaled = scaler.transform(x)
    proba    = model.predict(x_scaled, verbose=0)[0][0]
    outcome  = "⚠  HEART DISEASE DETECTED" if proba >= threshold else "✓  No Heart Disease"

    print(f"\n── Single Patient Prediction ──")
    for name, val in zip(FEATURE_NAMES, patient_values):
        print(f"  {name:12s}: {val}")
    print(f"\n  Disease Probability : {proba*100:.1f}%")
    print(f"  Prediction          : {outcome}")


