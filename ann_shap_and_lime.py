"""
=============================================================
  AI for Interpretable Heart Disease Prediction
  ANN (MLP) + SHAP Explainability
=============================================================

SHAP (SHapley Additive exPlanations) — What & Why:
  SHAP is rooted in cooperative game theory. For every prediction,
  it answers: "How much did each feature CONTRIBUTE to pushing the
  output above or below the baseline (average prediction)?"

  For a patient predicted at 78% disease risk:
    cp (chest pain)  → +0.31  (pushed prediction UP strongly)
    thalach          → +0.18  (pushed prediction UP)
    age              → -0.05  (pushed prediction slightly DOWN)
    ...and so on for all 13 features

  The SHAP values always sum to: prediction − baseline_prediction
  This is the "additive" guarantee — mathematically exact, not approximate.

SHAP Explainer choice for ANN — DeepExplainer:
  • DeepExplainer: designed specifically for TensorFlow/Keras.
    Uses backpropagation rules (DeepLIFT + Shapley values) to compute
    exact-ish SHAP values very efficiently. Best choice for our MLP.
  • KernelExplainer: model-agnostic fallback (works on anything),
    but very slow — estimates via random sampling. Used here as a
    backup if DeepExplainer hits a compatibility issue.

4 SHAP Visualisations produced:
  1. Summary Plot (Beeswarm) — global feature importance + direction
  2. Bar Plot               — mean |SHAP| ranked importance
  3. Waterfall Plot         — single patient explanation
  4. Heatmap               — SHAP values across all test patients
"""

