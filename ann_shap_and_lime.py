# ── 3. TRAIN / TEST SPLIT + SCALING ──────────────────────────────────────────
def split_and_scale(df: pd.DataFrame):
    X = df[FEATURE_NAMES].values
    y = df["target"].values

    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=0.2, random_state=RANDOM_STATE, stratify=y
    )

    scaler = StandardScaler()
    X_train = scaler.fit_transform(X_train)
    X_test  = scaler.transform(X_test)

    return X_train, X_test, y_train, y_test, scaler


# ── 4. BUILD ANN ──────────────────────────────────────────────────────────────
def build_model(input_dim: int = 13) -> keras.Model:
    model = keras.Sequential([
        layers.Input(shape=(input_dim,)),
        layers.Dense(64, activation="relu", kernel_initializer="he_normal", name="hidden_1"),
        layers.BatchNormalization(),
        layers.Dropout(0.3),
        layers.Dense(32, activation="relu", kernel_initializer="he_normal", name="hidden_2"),
        layers.BatchNormalization(),
        layers.Dropout(0.2),
        layers.Dense(16, activation="relu", kernel_initializer="he_normal", name="hidden_3"),
        layers.Dense(1, activation="sigmoid", name="output")
    ], name="MLP_HeartDisease")

    model.compile(
        optimizer=keras.optimizers.Adam(learning_rate=0.001),
        loss="binary_crossentropy",
        metrics=["accuracy", keras.metrics.AUC(name="auc")]
    )
    return model


# ── 5. TRAINING ───────────────────────────────────────────────────────────────
def train_model(model, X_train, y_train):
    early_stop = callbacks.EarlyStopping(
        monitor="val_loss", patience=20,
        restore_best_weights=True, verbose=1
    )
    reduce_lr = callbacks.ReduceLROnPlateau(
        monitor="val_loss", factor=0.5,
        patience=10, min_lr=1e-6, verbose=1
    )
    history = model.fit(
        X_train, y_train,
        epochs=200, batch_size=32,
        validation_split=0.15,
        callbacks=[early_stop, reduce_lr],
        verbose=1
    )
    return history


# ── 6. EVALUATION ─────────────────────────────────────────────────────────────
def evaluate(model, X_test, y_test) -> dict:
    y_proba = model.predict(X_test, verbose=0).flatten()
    y_pred  = (y_proba >= 0.5).astype(int)

    acc = accuracy_score(y_test, y_pred)
    auc = roc_auc_score(y_test, y_proba)

    print(f"\n── Model Performance ──")
    print(f"  Test Accuracy : {acc*100:.2f}%")
    print(f"  Test ROC-AUC  : {auc:.4f}")
    print(f"\nClassification Report:\n"
          f"{classification_report(y_test, y_pred, target_names=['No Disease', 'Disease'])}")

    return dict(y_pred=y_pred, y_proba=y_proba, acc=acc, auc=auc)


# ── 7. CROSS-VALIDATION ───────────────────────────────────────────────────────
def cross_validate_ann(X_train, y_train, n_splits: int = 5) -> np.ndarray:
    print(f"\n⏳ Running {n_splits}-Fold Cross Validation...")
    skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=RANDOM_STATE)
    fold_aucs = []

    for fold, (train_idx, val_idx) in enumerate(skf.split(X_train, y_train), 1):
        Xf_train, Xf_val = X_train[train_idx], X_train[val_idx]
        yf_train, yf_val = y_train[train_idx], y_train[val_idx]

        fold_model = build_model()
        fold_model.fit(
            Xf_train, yf_train,
            epochs=100, batch_size=32,
            validation_data=(Xf_val, yf_val),
            callbacks=[callbacks.EarlyStopping(monitor="val_loss",
                                               patience=15,
                                               restore_best_weights=True)],
            verbose=0
        )
        proba = fold_model.predict(Xf_val, verbose=0).flatten()
        auc   = roc_auc_score(yf_val, proba)
        fold_aucs.append(auc)
        print(f"  Fold {fold}: AUC = {auc:.4f}")

    cv = np.array(fold_aucs)
    print(f"  Mean AUC: {cv.mean():.4f} ± {cv.std():.4f}")
    return cv


