def compute_shap_values(model, X_train, X_test):
    """
    Compute SHAP values using DeepExplainer (primary) or
    KernelExplainer (fallback).

    DeepExplainer:
      Takes a background dataset (sample of training data) as the
      'baseline' — this is the average/expected prediction SHAP
      compares against. We use 100 random training samples as
      background; larger = more accurate but slower.

      Returns shap_values shape: (n_test_samples, 13)
      Each value = how much that feature pushed the prediction
      away from the background average, for that specific patient.

    Why 100 background samples?
      SHAP averages over all background samples to estimate the
      baseline. 100 is a good trade-off: stable estimates without
      taking 10+ minutes to compute.
    """
    print("\n⏳ Computing SHAP values (DeepExplainer)...")

    # Sample 100 background points from training set
    bg_idx = np.random.choice(X_train.shape[0], size=100, replace=False)
    background = X_train[bg_idx]

    try:
        # Primary: DeepExplainer — fast, Keras-native
        explainer   = shap.DeepExplainer(model, background)
        shap_values = explainer.shap_values(X_test)

        # shap_values may be a list (one array per output neuron)
        # For binary classification with 1 output neuron, unwrap it
        if isinstance(shap_values, list):
            shap_values = shap_values[0]

        # Squeeze output neuron dimension if present: (n, 13, 1) → (n, 13)
        if shap_values.ndim == 3:
            shap_values = shap_values[:, :, 0]

        print(f"✓ SHAP values computed via DeepExplainer | shape: {shap_values.shape}")

    except Exception as e:
        # Fallback: KernelExplainer — model-agnostic, slower
        print(f"⚠  DeepExplainer failed ({e}). Falling back to KernelExplainer...")
        predict_fn  = lambda x: model.predict(x, verbose=0).flatten()
        explainer   = shap.KernelExplainer(predict_fn, background)
        shap_values = explainer.shap_values(X_test, nsamples=200)
        print(f"✓ SHAP values computed via KernelExplainer | shape: {shap_values.shape}")

    return explainer, shap_values


def plot_shap_summary(shap_values, X_test, feature_names):
    """
    PLOT 1 — Beeswarm Summary Plot.

    Each dot = one patient in the test set.
    X-axis = SHAP value (impact on model output).
    Color  = actual feature value (red = high, blue = low).

    How to read it:
      • Features are ranked top-to-bottom by mean |SHAP| (most important first)
      • A dot on the RIGHT → that feature pushed the prediction TOWARD disease
      • A dot on the LEFT  → that feature pushed the prediction AWAY from disease
      • Red dots on the right = high feature values cause disease predictions
      • Blue dots on the right = low feature values cause disease predictions

    Example: if 'cp' (chest pain type) shows red dots on the right,
    it means HIGH chest pain type values strongly predict heart disease.
    """
    X_test_df = pd.DataFrame(X_test, columns=feature_names)

    plt.figure(figsize=(10, 7))
    shap.summary_plot(
        shap_values, X_test_df,
        feature_names=feature_names,
        plot_type="dot",        # beeswarm
        show=False,
        max_display=13          # show all 13 features
    )
    plt.title("SHAP Summary Plot — Feature Impact on Heart Disease Prediction\n"
              "(Red = high feature value, Blue = low feature value)",
              fontsize=12, pad=15)
    plt.tight_layout()
    plt.savefig("shap_summary_beeswarm.png", dpi=150, bbox_inches="tight")
    print("✓ Saved → shap_summary_beeswarm.png")
    plt.show()


def plot_shap_bar(shap_values, feature_names):
    """
    PLOT 2 — Global Bar Plot.

    Shows mean(|SHAP value|) per feature — the average magnitude
    of each feature's impact across ALL test patients.
    This is the cleanest single-chart answer to:
    "Which features does the ANN rely on most overall?"

    Unlike the beeswarm, this ignores direction and just shows
    average absolute importance — easier to present in a paper.
    """
    mean_shap = np.abs(shap_values).mean(axis=0)
    importance_df = (
        pd.DataFrame({"feature": feature_names, "mean_shap": mean_shap})
        .sort_values("mean_shap", ascending=True)
    )

    plt.figure(figsize=(9, 6))
    bars = plt.barh(importance_df["feature"], importance_df["mean_shap"],
                    color=plt.cm.RdYlGn_r(
                        importance_df["mean_shap"] / importance_df["mean_shap"].max()
                    ), edgecolor="white")
    plt.xlabel("Mean |SHAP Value|  (average impact on model output)")
    plt.title("SHAP Global Feature Importance\nANN — Heart Disease Prediction", fontsize=12)
    plt.grid(axis="x", alpha=0.3)
    plt.tight_layout()
    plt.savefig("shap_bar_global.png", dpi=150, bbox_inches="tight")
    print("✓ Saved → shap_bar_global.png")
    plt.show()


def plot_shap_waterfall(explainer, shap_values, X_test, feature_names,
                        patient_idx: int = 0):
    """
    PLOT 3 — Waterfall Plot for a Single Patient.

    This is the most powerful interpretability plot for a medical
    AI project. It answers:
    "WHY did the model predict X% heart disease for THIS patient?"

    Each bar = one feature's contribution:
      → Red bar going RIGHT = this feature INCREASED disease probability
      → Blue bar going LEFT  = this feature DECREASED disease probability

    The bottom starts at E[f(x)] — the model's average prediction
    across all patients (the baseline). Each feature's bar adds or
    subtracts from it, ending at f(x) — this patient's final prediction.

    patient_idx: which test patient to explain (0 = first patient).
    Change this to explain any specific patient you're curious about.
    """
    X_test_df = pd.DataFrame(X_test, columns=feature_names)

    # Get base value (expected model output)
    # DeepExplainer.expected_value can return a TF scalar tensor, a list,
    # a numpy array, or a plain float depending on the shap/TF version.
    # We force everything to a plain Python float to avoid TypeError.
    try:
        raw = explainer.expected_value
        if hasattr(raw, "numpy"):        # TF tensor → numpy
            raw = raw.numpy()
        raw = np.array(raw).flatten()    # list/array → 1-D
        base_value = float(raw[0])
    except Exception:
        # Ultimate fallback: mean model prediction on test set
        base_value = float(np.mean(
            explainer.model.predict(X_test, verbose=0).flatten()
        ))

    # Build SHAP Explanation object
    explanation = shap.Explanation(
        values      = shap_values[patient_idx],
        base_values = base_value,
        data        = X_test_df.iloc[patient_idx].values,
        feature_names = feature_names
    )

    pred_prob = float(np.array(base_value) +
                      np.sum(shap_values[patient_idx]))
    pred_prob = np.clip(pred_prob, 0, 1)
    outcome   = "DISEASE" if pred_prob >= 0.5 else "No Disease"

    plt.figure(figsize=(10, 6))
    shap.waterfall_plot(explanation, max_display=13, show=False)
    plt.title(f"SHAP Waterfall — Patient #{patient_idx}  |  "
              f"Prediction: {pred_prob*100:.1f}% → {outcome}",
              fontsize=11, pad=12)
    plt.tight_layout()
    plt.savefig(f"shap_waterfall_patient{patient_idx}.png",
                dpi=150, bbox_inches="tight")
    print(f"✓ Saved → shap_waterfall_patient{patient_idx}.png")
    plt.show()


def plot_shap_heatmap(shap_values, X_test, feature_names):
    """
    PLOT 4 — SHAP Heatmap across all test patients.

    Rows = features, Columns = patients (sorted by prediction).
    Color = SHAP value (red = pushed toward disease, blue = away).

    Useful for spotting PATTERNS across patients:
      • Do high-risk patients all share the same red features?
      • Are there subgroups with different risk profiles?
    """
    shap_df = pd.DataFrame(shap_values, columns=feature_names)

    # Sort patients by total SHAP sum (low risk → high risk left to right)
    shap_df = shap_df.iloc[shap_df.sum(axis=1).argsort().values]

