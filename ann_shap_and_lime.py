# ══════════════════════════════════════════════════════════════════════════════
#  LIME — LOCAL INTERPRETABLE MODEL-AGNOSTIC EXPLANATIONS
# ══════════════════════════════════════════════════════════════════════════════
"""
LIME vs SHAP — Why both?

SHAP:  globally consistent, mathematically grounded (Shapley values),
       shows how features contribute across ALL patients.

LIME:  locally faithful — fits a simple linear model around ONE patient's
       neighbourhood to explain THAT specific prediction.
       Answers: "If I slightly change this patient's values, how does the
       prediction change?" — great for individual clinical decisions.

Together they give:
  SHAP → "What does the model generally care about?"
  LIME → "Why THIS specific prediction for THIS patient?"

For a medical AI paper, having BOTH is the gold standard.
"""

def build_lime_explainer(X_train, feature_names):
    """
    Create a LIME tabular explainer.

    training_data: used to estimate the distribution of each feature
                   so LIME knows what 'nearby' samples look like.
    mode='classification': outputs probability of class 1 (disease).
    """
    import lime
    import lime.lime_tabular

    explainer = lime.lime_tabular.LimeTabularExplainer(
        training_data  = X_train,
        feature_names  = feature_names,
        class_names    = ["No Disease", "Disease"],
        mode           = "classification",
        discretize_continuous = True,   # bins continuous features for readability
        random_state   = RANDOM_STATE
    )
    print("✓ LIME explainer built.")
    return explainer


def explain_patient_lime(lime_explainer, model, X_test, scaler,
                         feature_names, patient_idx: int = 0,
                         num_features: int = 13):
    """
    Generate a LIME explanation for one patient.

    num_samples (inside LIME): 5000 perturbed neighbours are generated
    around the patient's feature values. A weighted linear model is fit
    to these neighbours — features with large linear coefficients are
    the most locally important.

    num_features: how many top features to display (we show all 13).

    Returns the explanation object (used for both plot and text report).
    """
    # predict_fn must take scaled input and return class probabilities
    def predict_fn(X_scaled):
        return model.predict(X_scaled, verbose=0).flatten()

    # LIME needs a predict function that returns [P(class0), P(class1)]
    def predict_proba_fn(X_scaled):
        p = model.predict(X_scaled, verbose=0).flatten()
        return np.column_stack([1 - p, p])

    patient = X_test[patient_idx]   # already scaled

    explanation = lime_explainer.explain_instance(
        data_row       = patient,
        predict_fn     = predict_proba_fn,
        num_features   = num_features,
        num_samples    = 5000,
        labels         = (1,)          # explain class 1 = Disease
    )

    return explanation


def plot_lime_bar(explanation, patient_idx: int = 0):
    """
    LIME Bar Chart — local feature importance for one patient.

    Each bar = how much that feature pushed the local linear model's
    prediction toward (+) or away from (-) heart disease.

    Unlike SHAP which uses exact Shapley values, LIME weights are
    coefficients of a locally-fitted linear model — they are
    approximate but very intuitive and fast to compute.
    """
    # Extract feature-weight pairs for class 1 (Disease)
    feat_weights = explanation.as_list(label=1)
    feat_weights.sort(key=lambda x: x[1])   # sort low → high

    features = [f[0] for f in feat_weights]
    weights  = [f[1] for f in feat_weights]
    colors   = ["#e74c3c" if w > 0 else "#2980b9" for w in weights]

    plt.figure(figsize=(10, 7))
    bars = plt.barh(features, weights, color=colors, edgecolor="white", alpha=0.85)
    plt.axvline(0, color="black", lw=1.2)
    plt.xlabel("LIME Weight  (+ = toward Disease,  - = away from Disease)")
    plt.title(
        f"LIME Local Feature Importance — Patient #{patient_idx}\n"
        f"(Linear approximation in the neighbourhood of this patient)",
        fontsize=12
    )
    plt.grid(axis="x", alpha=0.3)
    plt.tight_layout()
    plt.savefig(f"lime_bar_patient{patient_idx}.png", dpi=150, bbox_inches="tight")
    print(f"✓ Saved → lime_bar_patient{patient_idx}.png")
    plt.show()


