def shap_patient_report(shap_values, X_test, feature_names,
                        scaler, patient_idx: int = 0):
    """
    Print a readable clinical-style SHAP report for one patient.
    Shows original (unscaled) feature values alongside SHAP contributions.
    """
    # Inverse-transform to get original feature values
    original_values = scaler.inverse_transform(X_test)[patient_idx]
    sv = shap_values[patient_idx]

    report = pd.DataFrame({
        "Feature"      : feature_names,
        "Value"        : original_values.round(2),
        "SHAP"         : sv.round(4),
        "Direction"    : ["↑ Risk" if s > 0 else "↓ Risk" for s in sv]
    }).sort_values("SHAP", key=abs, ascending=False)

    print(f"\n── SHAP Clinical Report — Patient #{patient_idx} ──")
    print(report.to_string(index=False))
    risk_rows       = report[report["SHAP"] > 0]
    protective_rows = report[report["SHAP"] < 0]
    top_risk        = risk_rows.iloc[0]["Feature"]       if len(risk_rows) > 0       else "None"
    top_protective  = protective_rows.iloc[0]["Feature"] if len(protective_rows) > 0 else "None"
    top_risk_shap   = risk_rows.iloc[0]["SHAP"]          if len(risk_rows) > 0       else 0.0
    top_prot_shap   = protective_rows.iloc[0]["SHAP"]    if len(protective_rows) > 0 else 0.0

    print(f"\n  Top risk factor : {top_risk} (SHAP={top_risk_shap:+.4f}  → pushes TOWARD disease)")
    print(f"  Top protective  : {top_protective} (SHAP={top_prot_shap:+.4f}  → pushes AWAY from disease)")


# ── 9. SHAP FORCE PLOT ───────────────────────────────────────────────────────
def plot_shap_force(explainer, shap_values, X_test, feature_names,
                    scaler, patient_idx: int = 0):
    """
    SHAP Force Plot — horizontal arrow visualisation for one patient.

    Difference from Waterfall:
      Waterfall  → vertical stacked bars, precise SHAP values shown
      Force Plot → horizontal push/pull arrows, more intuitive at a glance
                   Red arrows push RIGHT  → toward disease
                   Blue arrows push LEFT  → away from disease
      The base value (E[f(x)]) is in the centre. The final prediction
      is where all the arrows land on the right.

    This is the most visually striking single-patient explanation —
    ideal for PPT slides, posters, and paper figures.

    Two outputs:
      1. Static matplotlib version  → saved as PNG (works everywhere)
      2. Interactive HTML version   → saved as HTML (opens in browser,
                                      hover over features for details)
    """
    # Recover original feature values for readable display
    original = scaler.inverse_transform(X_test)[patient_idx]
    X_display = pd.DataFrame([original], columns=feature_names)

    # Get base value — same robust extraction as waterfall
    try:
        raw = explainer.expected_value
        if hasattr(raw, "numpy"):
            raw = raw.numpy()
        base_value = float(np.array(raw).flatten()[0])
    except Exception:
        base_value = float(np.mean(
            explainer.model.predict(X_test, verbose=0).flatten()
        ))

    sv      = shap_values[patient_idx]          # shape (13,)
    pred    = float(np.clip(base_value + np.sum(sv), 0, 1))
    outcome = "DISEASE" if pred >= 0.5 else "No Disease"

    # ── Static matplotlib force plot ─────────────────────────────────────
    fig, ax = plt.subplots(figsize=(14, 3))
    ax.set_xlim(0, 1)
    ax.set_ylim(-1, 1)
    ax.axis("off")

    # Sort features: positive SHAP (risk) left-to-right, negative right-to-left
    pos_mask = sv > 0
    neg_mask = sv < 0

    pos_feats = [(feature_names[i], sv[i], original[i]) for i in range(len(sv)) if pos_mask[i]]
    neg_feats = [(feature_names[i], sv[i], original[i]) for i in range(len(sv)) if neg_mask[i]]
    pos_feats.sort(key=lambda x: -x[1])   # largest positive first
    neg_feats.sort(key=lambda x:  x[1])   # most negative first

    # Draw baseline marker
    ax.axvline(base_value, color="gray", lw=1.5, linestyle="--", alpha=0.6)
    ax.text(base_value, 0.75, f"Base\n{base_value:.2f}",
            ha="center", va="bottom", fontsize=8, color="gray")

    # Draw prediction marker
    ax.axvline(pred, color="black", lw=2)
    ax.text(pred, 0.75, f"Pred\n{pred:.2f}",
            ha="center", va="bottom", fontsize=9, fontweight="bold")

    # Draw positive (red) arrows — stacked from base rightward
    cursor = base_value
    bar_h  = 0.35
    for name, val, feat_val in pos_feats:
        width = val
        ax.barh(0, width, left=cursor, height=bar_h,
                color="#e74c3c", alpha=0.85, edgecolor="white")
        if abs(width) > 0.02:
            ax.text(cursor + width/2, 0, f"{name}\n={feat_val:.1f}",
                    ha="center", va="center", fontsize=7,
                    color="white", fontweight="bold")
        cursor += width

    # Draw negative (blue) arrows — stacked from base leftward
    cursor = base_value
    for name, val, feat_val in neg_feats:
        width = abs(val)
        ax.barh(0, -width, left=cursor, height=bar_h,
                color="#2980b9", alpha=0.85, edgecolor="white")
        if width > 0.02:
            ax.text(cursor - width/2, 0, f"{name}\n={feat_val:.1f}",
                    ha="center", va="center", fontsize=7,
                    color="white", fontweight="bold")
        cursor -= width

    ax.set_title(
        f"SHAP Force Plot — Patient #{patient_idx}  |  "
        f"Prediction: {pred*100:.1f}%  →  {outcome}\n"
        f"Red = pushes TOWARD disease  |  Blue = pushes AWAY from disease",
        fontsize=11, pad=10
    )
    plt.tight_layout()
    plt.savefig(f"shap_force_patient{patient_idx}.png", dpi=150, bbox_inches="tight")
    print(f"✓ Saved → shap_force_patient{patient_idx}.png")
    plt.show()

    # ── Interactive HTML force plot (bonus) ──────────────────────────────
